package integer;

public class App {

	public static void main(String[] args) {
		// 변수 만들기 : 1. 변수 선언(자료형) 2. 변수 초기화
		
		int x;	// int는 정수 자료형 선언
		x = 7;	// x의 값을 초기화
		int y = 10;	// y는 정수 자료형 선언 및 값 초기화 한번에
		int z = x + y;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);	// 한줄복사 CTRL+ALT+아래쪽화살표
	}

}
